# math operators + - / * % ** // 

# addition (+)
num1 = 3
num2 = 5
sum = 3 + 5

print(f"The sum is: {sum}")

# subtraction (-)
difference = num1 - num2
print(f"The difference is: {difference}")

# multiplication (*)
product = num1 * num2
print(f"The product is: {product}")

# division (/)
quotient = num1 / num2
print(f"The quotient is: {quotient}")

# modulo (%) (remainder; division)
remainder = num1 % num2
print(f"the remainder is: {remainder}")
num3 = 24
num4= 12
remainder2 = num3 % num4
print(f"the remainder is: {remainder2}")

# floor division (//) (rounds decimals)
num5 = 10
num6 = 11
num7 = 12
num8 = 3
print(num5//num8)
print(num6//num8)
print(num7//num8)

print(int(num5/num8))
